//! \brief Base class for Cooling Functions
class CoolingFunctionBase {
 public:
  explicit CoolingFunctionBase(ParameterInput *pin);
  ~CoolingFunctionBase();

  void Initialize(Real mu, Real muH);
  // Real tcool(const Real T, const Real nH);
  // Real netcool(const Real T, const Real nH);

  // Real Getdt(const Real T, const Real nH);
  virtual Real Lambda_T(const Real rho, const Real Press) = 0;
  virtual Real dlnL_dlnT(const Real rho, const Real Press) = 0;
  virtual Real Gamma_T(const Real rho, const Real Press) = 0;
  virtual Real Get_mu(const Real rho, const Real Press) = 0;
  virtual Real Get_muH() = 0;

  virtual Real GetTemperature(const Real rho, const Real Press) = 0;

  // accessors
  Real Get_Tmax() { return T_max; }
  Real Get_Tfloor() { return T_floor; }

  Units *punit;

  Real to_nH,to_pok;
  Real mean_mass_per_H;

  Real T_max, T_floor, cfl_cool, gamma_adi;

 private:
  Real mu,muH;
};

//========================================================================================
//! \class PiecewiseLinearFits
//! \brief Cooling function with Piecewise Linear Fits used in El-Badry et al. 2019
//!        provided by Drummond Fielding
//! \note
//! - constant PE heating is applied at T<T_PE (Gamma and T_PE must set in input)
//! - mu = 0.62 (fixed), muH = 1.4
//! - not very good for low-T cooling (T<T_PE)
//========================================================================================
class PiecewiseLinearFits : public CoolingFunctionBase {
 public:
  explicit PiecewiseLinearFits(ParameterInput *pin);
  // ~PiecewiseLinearFits() override { delete punit; }

  Real Lambda_T(const Real rho, const Real Press) override;
  Real dlnL_dlnT(const Real rho, const Real Press) override;
  Real Gamma_T(const Real rho, const Real Press) override;
  Real Get_mu(const Real rho, const Real Press) override { return mu; }
  Real Get_muH() override { return muH; }

  Real GetTemperature(const Real rho, const Real Press) override;

 private:
  Real mu,muH;
  Real T_PE,Gamma0;
  // fitting parameters
  static constexpr int nfit_cool = 12;
  Real T_cooling_curve[nfit_cool] =
    {0.99999999e1,
     1.0e+02, 6.0e+03, 1.75e+04,
     4.0e+04, 8.7e+04, 2.30e+05,
     3.6e+05, 1.5e+06, 3.50e+06,
     2.6e+07, 1.0e+12};

  Real lambda_cooling_curve[nfit_cool] =
    { 1e-30,
      1.00e-27,   2.00e-26,   1.50e-22,
      1.20e-22,   5.25e-22,   5.20e-22,
      2.25e-22,   1.25e-22,   3.50e-23,
      2.10e-23,   4.12e-21};

  Real exponent_cooling_curve[nfit_cool] =
    {3.,
     0.73167566,  8.33549431, -0.26992783,
     1.89942352, -0.00984338, -1.8698263 ,
     -0.41187018, -1.50238273, -0.25473349,
     0.5000359, 0.5 };
};

